<?php


namespace SWP\Bundle\CoreBundle\Model;


class PWAConfig
{
    private ?string $url = null;

    /**
     * @return string
     */
    public function getUrl(): ?string
    {
        return $this->url;
    }

    /**
     * @param string $url
     */
    public function setUrl(string $url): void
    {
        $this->url = $url;
    }

}